                    guard value == 1 else { continue }
                    path.addRect(CGRect(
                        x: CGFloat(col) * pixelSize,
                        y: CGFloat(row) * pixelSize,
                        width: pixelSize < 2 ? pixelSize : pixelSize + 0.5,
                        height: pixelSize < 2 ? pixelSize : pixelSize + 0.5
                    ))
                }
            }
            context.fill(path, with: .color(color))
        }
        .frame(
            width: CGFloat(31) * pixelSize,
            height: CGFloat(13) * pixelSize
        )
    }

